Route::post('/register', function (Request $request) {

    $user = User::create([
        'name' => $request->name,
        'email' => $request->email,
        'password' => bcrypt($request->password),
    ]);

    SendEmailJob::dispatch($user);

    return response()->json(['message' => 'User registered, email will be sent']);
});
